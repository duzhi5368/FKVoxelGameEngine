using FKVoxelEngine.Framework;
using System;
// ===============================================================================
namespace FKVoxelClient.Language
{
    public static class FKLanguage_Default
    {
        public static void AddDefault()
        {
            IFKLanguageManagerService service = (IFKLanguageManagerService)FKEngine.GetInstance.Game.Services.GetService(typeof(IFKLanguageManagerService));
            if (service == null)
                throw new NullReferenceException("Can not find language manager component.");

            service.Add("en", "MainMenu_Start", "Start");
        }
    }
}