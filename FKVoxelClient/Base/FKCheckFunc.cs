namespace FKVoxelClient.Base
{
    public static class FKCheckFunc
    {
        public static int MinNameLength = 4;
        public static int MaxNameLength = 16;
        public static string AllowedNameChars = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz1234567890_-";
        public static bool IsValidName(string s)
        {
            if (s.Length < MinNameLength || s.Length > MaxNameLength)
            {
                return false;
            }
            for (int i = 0; i < s.Length; i++)
            {
                if (!AllowedNameChars.Contains(s[i].ToString()))
                {
                    return false;
                }
            }
            return true;
        }
    }
}