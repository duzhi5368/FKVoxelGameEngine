using FKVoxelEngine.Graphics;
using System;
// ===============================================================================
namespace FKVoxelEngine.Config
{
    public class FKEffectConfig
    {
        public bool BloomEnabled { get; set; }
        public ENUM_FKBloomState BloomState { get; set; }

        internal FKEffectConfig()
        {
            BloomEnabled = false;
            BloomState = ENUM_FKBloomState.eDefault;
        }

        internal bool Setup()
        {
            return true;
        }

        public override string ToString()
        {
            string ret = string.Empty;

            ret += $"BloomEnabled = {BloomEnabled}\n";
            ret += $"BloomState = {BloomState}\n";

            return ret;
        }
    }
}