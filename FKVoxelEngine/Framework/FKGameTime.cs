namespace FKVoxelEngine.Framework
{
    public static class FKGameTime
    {
        private const float RealTime_SecondsInOneDay = 24 * 60 * 60;
        private const float GameHour_InRealTimeMinutes = (float)2 / 60;
        private const float GameHour_InRealTimeSeconds = GameHour_InRealTimeMinutes * 60;

        public static float GetGameTimeOfDay()
        {
            return (float)((DateTime.Now.TimeOfDay.TotalSeconds / GameHour_InRealTimeSeconds) % 24);
        }
    }
