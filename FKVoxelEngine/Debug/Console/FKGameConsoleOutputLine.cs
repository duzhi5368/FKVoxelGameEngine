namespace FKVoxelEngine.Debug
{
    /// <summary>
    /// 控制台输出一行的类型
    /// </summary>
    public enum ENUM_OutputLineType
    {
        eCommand,       // 控制台输出可能是命令
        eOutput,        // 也可能是处理结果的输出
    }
    class FKGameConsoleOutputLine
    {
        public string Output { get; set; }
        public ENUM_OutputLineType Type { get; set; }

        public FKGameConsoleOutputLine(string output, ENUM_OutputLineType type)
        {
            Output = output;
            Type = type;
        }

        public override string ToString()
        {
            return Output;
        }
    }
}