using System;
// ===============================================================================
namespace FKVoxelEngine.RenderObj
{
    public class FKBlock
    {
        public ENUM_BlockType Type { get; set; }


        protected FKBlock(ENUM_BlockType type)
        {
            Type = type;
        }

        public bool Exists
        {
            get { return Type != ENUM_BlockType.eNone; }
        }

        public static FKBlock Empty
        {
            get { return new FKBlock(ENUM_BlockType.eNone); }
        }

        public override string ToString()
        {
            return Type.ToString();
        }
    }
}